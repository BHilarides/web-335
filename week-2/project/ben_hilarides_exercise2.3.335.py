# Defining the quote as a string variable
quote = "You have power over your mind - not outside events. Realize this, and you will find strength."

# Defining the author attribution
author = "Marcus Aurelius"

# Printing the quote with proper formatting
print(f'"{quote}"')

# Printing author attribution with a preceding dash
print(f' - {author}')

# Adding blank line for readability
print()

# Printing additional context about quote
print("This quote is from 'Meditations', written by Marcus Aurelius in 161-180 AD.")